// initialization
void adxl_init (void) {
	adxl_read(0x00,1); // 0xE5

	adxl_write (0x2d, 0); // reset all bits
	adxl_write (0x2d, 0x08); // measure bit-1, wakeup 0, 0 at 8hz

	adxl_write (0x31, 0x01); // +- 4g range
}

